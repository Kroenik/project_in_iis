create table public.volunteer_profiles (
  id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  city text null,
  zip_code bigint null,
  availability json null,
  skills text[] null,
  languages text[] null,
  h_week bigint null,
  start_date date null,
  end_date date null,
  recurring boolean null,
  preference text null,
  preference_embedding extensions.vector null,
  user_id uuid null,
  name text null,
  contact text null,
  constraint volunteer_profiles_pkey primary key (id),
  constraint volunteer_profiles_user_id_fkey foreign KEY (user_id) references auth.users (id)
) TABLESPACE pg_default;